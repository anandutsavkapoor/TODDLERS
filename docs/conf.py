# Class docstrings list methods (Napoleon "Methods" section) and autodoc also documents
# them via :members:; silence the resulting duplicate-object-description warnings.
import sphinx.domains.python
_orig_note_object = sphinx.domains.python.PythonDomain.note_object
def _quiet_note_object(self, name, objtype, node_id, aliased=False, location=None):
    if name in self.objects:
        return
    _orig_note_object(self, name, objtype, node_id, aliased=aliased, location=location)
sphinx.domains.python.PythonDomain.note_object = _quiet_note_object

