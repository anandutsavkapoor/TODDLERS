"""Sphinx configuration for the TODDLERS public package.

The package is a single importable ``toddlers`` package (pip-installed), so the API
reference is generated by running ``sphinx-apidoc`` over ``src/toddlers`` once at build
time (see ``setup`` below). The vendored ``toddlers.pts`` subpackage (third-party, AGPL)
is excluded.
"""
import os
import sys

# Make the package importable for autodoc even without an install (e.g. local builds).
# On Read the Docs the package is pip-installed via .readthedocs.yaml, so this is a no-op there.
sys.path.insert(0, os.path.abspath("../src"))


def setup(app):
    """Generate the API .rst stubs from the package before the build."""
    import sphinx.ext.apidoc

    here = os.path.dirname(os.path.abspath(__file__))
    pkg_dir = os.path.abspath(os.path.join(here, "..", "src", "toddlers"))
    out_dir = os.path.join(here, "api")
    pts_dir = os.path.join(pkg_dir, "pts")          # vendored third-party (AGPL) -> exclude

    sphinx.ext.apidoc.main([
        "--force",          # regenerate every build
        "--module-first",   # module docstring before submodule listing
        "--separate",       # one page per module (cleaner navigation)
        "-o", out_dir,
        pkg_dir,
        pts_dir,            # exclude pattern
    ])
    return app


